package com.icbc.nt.bean;

public class RpBean extends BeanParent{
	private String userId;
	private String userName;
	private String rpTime;
	private String assptId;
	private String rpPoint;
	private String rpMsg;
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getRpTime() {
		return rpTime;
	}
	public void setRpTime(String rpTime) {
		this.rpTime = rpTime;
	}
	
	public String getAssptId() {
		return assptId;
	}
	public void setAssptId(String assptId) {
		this.assptId = assptId;
	}
	public String getRpPoint() {
		return rpPoint;
	}
	public void setRpPoint(String rpPoint) {
		this.rpPoint = rpPoint;
	}
	public String getRpMsg() {
		return rpMsg;
	}
	public void setRpMsg(String rpMsg) {
		this.rpMsg = rpMsg;
	}
	
	
}
